package frr

import (
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"strings"

	"github.com/hk59775634/qosnat2/internal/store"
)

const (
	Dir              = "/etc/qosnat2/frr"
	ManagedRoutes    = Dir + "/managed-routes.conf"
	ExtraConfig      = Dir + "/extra.conf"
	IncludeSnippet   = "/etc/frr/frr.conf.d/qosnat2.conf"
	ApplyVTYSHScript = Dir + "/apply.vtysh"
)

// Installed 报告 frr 包与 systemd 单元是否可用（兼容旧调用）。
func Installed() bool {
	return PackageInstalled() && ServiceActive()
}

func EditableConfigKeys() []string {
	return []string{"frr.conf", "extra", "managed", "daemons", "include"}
}

// RenderManaged 从 qosnat2 托管路由生成 staticd/vtysh 配置片段。
func RenderManaged(routes []store.RouteEntry) (string, error) {
	var b strings.Builder
	b.WriteString("! generated by qosnat2 — do not edit\n")
	b.WriteString("configure terminal\n")
	for _, r := range routes {
		if !r.Enabled {
			continue
		}
		lines, err := routeVTYLines(r)
		if err != nil {
			return "", fmt.Errorf("%s: %w", r.ID, err)
		}
		for _, ln := range lines {
			b.WriteString(ln)
			b.WriteByte('\n')
		}
	}
	b.WriteString("end\n")
	return b.String(), nil
}

func routeVTYLines(r store.RouteEntry) ([]string, error) {
	dest, err := store.NormalizeRouteDest(r.Dest)
	if err != nil {
		return nil, err
	}
	if dest == "default" {
		dest = "0.0.0.0/0"
	}
	table := r.Table
	suffix := ""
	if table > 0 && table != 254 {
		suffix = fmt.Sprintf(" table %d", table)
	}
	var out []string
	if len(r.Nexthops) > 0 {
		for _, nh := range r.Nexthops {
			ln, err := singleVTYLine(dest, nh.Gateway, nh.Device, nh.Weight, suffix)
			if err != nil {
				return nil, err
			}
			out = append(out, ln)
		}
		return out, nil
	}
	ln, err := singleVTYLine(dest, r.Gateway, r.Device, 0, suffix)
	if err != nil {
		return nil, err
	}
	return []string{ln}, nil
}

func singleVTYLine(dest, gw, dev string, _ int, tableSuffix string) (string, error) {
	gw = strings.TrimSpace(gw)
	dev = strings.TrimSpace(dev)
	if gw == "" && dev == "" {
		return "", fmt.Errorf("route requires gateway or device")
	}
	var nh string
	if gw != "" && dev != "" {
		nh = fmt.Sprintf("%s %s", gw, dev)
	} else if gw != "" {
		nh = gw
	} else {
		nh = dev
	}
	line := fmt.Sprintf("ip route %s %s%s", dest, nh, tableSuffix)
	return line, nil
}

// ApplyManaged 写入托管路由并 reload FRR（zebra 批量 netlink，较逐条 ip 更平滑）。
func ApplyManaged(routes []store.RouteEntry) error {
	body, err := RenderManaged(routes)
	if err != nil {
		return err
	}
	if err := os.MkdirAll(Dir, 0755); err != nil {
		return err
	}
	if err := os.WriteFile(ManagedRoutes, []byte(body), 0644); err != nil {
		return err
	}
	if err := ensureInclude(); err != nil {
		return err
	}
	script := strings.TrimSpace(body) + "\nwrite memory\n"
	if err := os.WriteFile(ApplyVTYSHScript, []byte(script), 0600); err != nil {
		return err
	}
	out, err := exec.Command("vtysh", "-f", ApplyVTYSHScript).CombinedOutput()
	if err != nil {
		return fmt.Errorf("vtysh apply: %s %w", strings.TrimSpace(string(out)), err)
	}
	return nil
}

func ensureInclude() error {
	if err := os.MkdirAll(filepath.Dir(IncludeSnippet), 0755); err != nil {
		return err
	}
	includeLine := "include " + ManagedRoutes + "\n"
	if extra, err := os.ReadFile(ExtraConfig); err == nil && strings.TrimSpace(string(extra)) != "" {
		includeLine += "include " + ExtraConfig + "\n"
	}
	cur, err := os.ReadFile(IncludeSnippet)
	if err == nil && string(cur) == includeLine {
		return nil
	}
	return os.WriteFile(IncludeSnippet, []byte("! qosnat2 managed FRR includes\n"+includeLine), 0644)
}

// ReadExtra 读取用户附加 FRR 配置。
func ReadExtra() (string, error) {
	b, err := os.ReadFile(ExtraConfig)
	if os.IsNotExist(err) {
		return "", nil
	}
	return string(b), err
}

// WriteExtra 保存用户附加 FRR 配置（vtysh/frr.conf 语法）。
func WriteExtra(body string) error {
	if err := os.MkdirAll(Dir, 0755); err != nil {
		return err
	}
	if strings.TrimSpace(body) == "" {
		_ = os.Remove(ExtraConfig)
		return ensureInclude()
	}
	if err := os.WriteFile(ExtraConfig, []byte(body), 0644); err != nil {
		return err
	}
	return ensureInclude()
}
