const DynamicRoutingConfig = Dir + "/dynamic-routing.conf"
const dynamicApplyScript = Dir + "/dynamic-routing-apply.vtysh"

// RenderDynamic 生成 FRR 原生配置（供 include 与 vtysh 回放）。
func RenderDynamic(dr store.DynamicRoutingState) string {
	var b strings.Builder
	b.WriteString("! generated by qosnat2 — dynamic routing\n")
	renderBGPConfig(&b, dr.BGP)
	renderOSPFConfig(&b, dr.OSPF)
	return b.String()
}

// RenderDynamicApplyScript 生成 vtysh 批量应用脚本（先清理再写入）。
func RenderDynamicApplyScript(dr store.DynamicRoutingState) string {
	var b strings.Builder
	b.WriteString("configure terminal\n")
	b.WriteString("no router bgp\n")
	b.WriteString("no router ospf\n")
	if dr.BGP.Enabled {
		writeBGPVTY(&b, dr.BGP)
	}
	if dr.OSPF.Enabled {
		writeOSPFVTY(&b, dr.OSPF)
	}
	b.WriteString("end\n")
	b.WriteString("write memory\n")
	return b.String()
}

func renderBGPConfig(b *strings.Builder, cfg store.BGPConfig) {
	if !cfg.Enabled || cfg.ASN == 0 {
		return
	}
	fmt.Fprintf(b, "!\nrouter bgp %d\n", cfg.ASN)
	if cfg.RouterID != "" {
		fmt.Fprintf(b, " bgp router-id %s\n", cfg.RouterID)
	}
	for _, n := range cfg.Neighbors {
		if !n.Enabled {
			continue
		}
		fmt.Fprintf(b, " neighbor %s remote-as %d\n", n.Address, n.RemoteASN)
		if n.Description != "" {
			fmt.Fprintf(b, " neighbor %s description %s\n", n.Address, quoteDesc(n.Description))
		}
		if n.Password != "" {
			fmt.Fprintf(b, " neighbor %s password %s\n", n.Address, n.Password)
		}
		if n.UpdateSource != "" {
			fmt.Fprintf(b, " neighbor %s update-source %s\n", n.Address, n.UpdateSource)
		}
	}
	b.WriteString(" !\n address-family ipv4 unicast\n")
	for _, n := range cfg.Neighbors {
		if n.Enabled {
			fmt.Fprintf(b, "  neighbor %s activate\n", n.Address)
		}
	}
	for _, cidr := range cfg.Networks {
		fmt.Fprintf(b, "  network %s\n", cidr)
	}
	if cfg.RedistributeConnected {
		b.WriteString("  redistribute connected\n")
	}
	b.WriteString(" exit-address-family\n")
	b.WriteString("!\n")
}

func renderOSPFConfig(b *strings.Builder, cfg store.OSPFConfig) {
	if !cfg.Enabled {
		return
	}
	b.WriteString("!\nrouter ospf\n")
	if cfg.RouterID != "" {
		fmt.Fprintf(b, " ospf router-id %s\n", cfg.RouterID)
	}
	for _, n := range cfg.Networks {
		if !n.Enabled {
			continue
		}
		area := n.Area
		if area == "" {
			area = "0.0.0.0"
		}
		fmt.Fprintf(b, " network %s area %s\n", n.Prefix, area)
	}
	if cfg.RedistributeConnected {
		b.WriteString(" redistribute connected\n")
	}
	b.WriteString("!\n")
}

