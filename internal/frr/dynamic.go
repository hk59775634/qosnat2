func quoteDesc(s string) string {
	if strings.ContainsAny(s, " \t\n\r\"'") {
		return `"` + strings.ReplaceAll(s, `"`, `\"`) + `"`
	}
	return s
}

// ApplyDynamic 写入动态路由配置、同步 daemons 并通过 vtysh 应用。
func ApplyDynamic(dr store.DynamicRoutingState) error {
	if err := os.MkdirAll(Dir, 0755); err != nil {
		return err
	}
	native := RenderDynamic(dr)
	if err := os.WriteFile(DynamicRoutingConfig, []byte(native), 0644); err != nil {
		return err
	}
	if err := ensureInclude(); err != nil {
		return err
	}
	restarted, err := SyncDaemons(store.DynamicRoutingNeedsBGP(dr), store.DynamicRoutingNeedsOSPF(dr))
	if err != nil {
		return err
	}
	if !PackageInstalled() {
		return nil
	}
	if restarted {
		return nil
	}
	if !ServiceActive() {
		return nil
	}
	script := RenderDynamicApplyScript(dr)
	if err := os.WriteFile(dynamicApplyScript, []byte(script), 0600); err != nil {
		return err
	}
	if err := runVTYSH(script); err != nil {
		return fmt.Errorf("vtysh dynamic apply: %w", err)
	}
	return nil
}

// DynamicStatus 解析 BGP/OSPF 运行摘要（FRR 未运行或未安装时返回空字段）。
func DynamicStatus(dr store.DynamicRoutingState) map[string]any {
	st := map[string]any{
		"bgp":  map[string]any{"enabled": dr.BGP.Enabled, "summary": "", "neighbors": []any{}},
		"ospf": map[string]any{"enabled": dr.OSPF.Enabled, "summary": "", "neighbors": []any{}},
	}
	if !PackageInstalled() || !ServiceActive() {
		return st
	}
	if dr.BGP.Enabled {
		st["bgp"] = parseBGPSummary()
	}
	if dr.OSPF.Enabled {
		st["ospf"] = parseOSPFSummary()
	}
	return st
}

func parseBGPSummary() map[string]any {
	out := map[string]any{"summary": "", "neighbors": []map[string]string{}}
	raw, err := exec.Command("vtysh", "-c", "show ip bgp summary").CombinedOutput()
	if err != nil {
		out["summary"] = strings.TrimSpace(string(raw))
		return out
	}
	text := string(raw)
	out["summary"] = strings.TrimSpace(text)
	out["neighbors"] = parseBGPNeighborTable(text)
	return out
}

func parseOSPFSummary() map[string]any {
	out := map[string]any{"summary": "", "neighbors": []map[string]string{}}
	raw, err := exec.Command("vtysh", "-c", "show ip ospf neighbor").CombinedOutput()
	if err != nil {
		out["summary"] = strings.TrimSpace(string(raw))
		return out
	}
	text := string(raw)
	out["summary"] = strings.TrimSpace(text)
	out["neighbors"] = parseOSPFNeighborTable(text)
	return out
}

func parseBGPNeighborTable(text string) []map[string]string {
	var rows []map[string]string
	for _, line := range strings.Split(text, "\n") {
		line = strings.TrimSpace(line)
		if line == "" || strings.HasPrefix(line, "Neighbor") || strings.HasPrefix(line, "-") {
			continue
		}
		fields := strings.Fields(line)
		if len(fields) < 9 {
			continue
		}
		rows = append(rows, map[string]string{
			"neighbor": fields[0],
			"v":        fields[1],
			"as":       fields[2],
			"state":    fields[9],
		})
	}
	return rows
}

func parseOSPFNeighborTable(text string) []map[string]string {
	var rows []map[string]string
	for _, line := range strings.Split(text, "\n") {
		line = strings.TrimSpace(line)
		if line == "" || strings.HasPrefix(line, "Neighbor") || strings.HasPrefix(line, "-") {
			continue
		}
		fields := strings.Fields(line)
		if len(fields) < 6 {
			continue
		}
		rows = append(rows, map[string]string{
			"neighbor_id": fields[0],
			"priority":    fields[1],
			"state":       fields[2],
			"dead_time":   fields[3],
			"address":     fields[4],
			"interface":   fields[5],
		})
	}
	return rows
}
