package nft

import (
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"sort"
	"strconv"
	"strings"

	"github.com/hk59775634/qosnat2/internal/store"
)

const (
	RulesPath = "/etc/qosnat2/nftables-qosnat.nft"
	TableName = "qosnat"
)

// Config 运行期网卡名（来自环境变量）
type Config struct {
	DevLAN string
	DevWAN string
}

// Render 根据 state 生成 inet qosnat 规则（无 flowtable，宿主机 WAN）
func Render(cfg Config, st store.State) (string, error) {
	if cfg.DevLAN == "" || cfg.DevWAN == "" {
		return "", fmt.Errorf("DEV_LAN and DEV_WAN required")
	}
	routes := st.PolicyRoutes
	if len(routes) == 0 {
		routes = []string{st.Shaper.PolicyCIDR}
	}
	if len(routes) == 0 {
		routes = []string{"10.0.0.0/8"}
	}
	ips := st.SharedIPs
	if len(ips) == 0 {
		return "", fmt.Errorf("shared_ips empty: configure at least one WAN SNAT address")
	}

	var b strings.Builder
	b.WriteString("# qosnat2 — generated by qosnatd\n")
	b.WriteString("# mark 隔离: 禁止 meta mark set / flowtable；QoS 用 tc_classid，IFB 用 bpf_redirect\n")
	b.WriteString("flush ruleset\n\n")
	b.WriteString(fmt.Sprintf("table inet %s {\n", TableName))

	// prerouting DNAT
	b.WriteString("    chain prerouting {\n")
	b.WriteString("        type nat hook prerouting priority dstnat; policy accept;\n")
	for _, f := range st.Firewall.WanPortForwards {
		proto := strings.ToLower(f.Proto)
		if proto == "" {
			proto = "tcp"
		}
		host := f.HostIP
		if host == "" {
			host = "127.0.0.1"
		}
		b.WriteString(fmt.Sprintf(
			"        iifname \"%s\" %s dport %d dnat to %s:%d\n",
			cfg.DevWAN, proto, f.WanPort, host, f.HostPort,
		))
	}
	b.WriteString("    }\n\n")

	// postrouting SNAT
	b.WriteString("    chain postrouting {\n")
	b.WriteString("        type nat hook postrouting priority srcnat; policy accept;\n")
	staticKeys := sortedKeys(st.StaticMappings)
	for _, inner := range staticKeys {
		b.WriteString(fmt.Sprintf("        ip saddr %s snat to %s\n", inner, st.StaticMappings[inner]))
	}
	if len(st.PrefixMappings) > 0 {
		b.WriteString(fmt.Sprintf("        oifname \"%s\" snat ip prefix to ip saddr map {\n", cfg.DevWAN))
		for _, inner := range sortedKeys(st.PrefixMappings) {
			b.WriteString(fmt.Sprintf("            %s : %s,\n", inner, st.PrefixMappings[inner]))
		}
		b.WriteString("        }\n")
	}
	var inline []string
	for i, ip := range ips {
		inline = append(inline, fmt.Sprintf("%d : %s", i, ip))
	}
	for _, cidr := range routes {
		b.WriteString(fmt.Sprintf(
			"        ip saddr %s oifname \"%s\" snat to numgen inc mod %d map { %s }\n",
			cidr, cfg.DevWAN, len(ips), strings.Join(inline, ", "),
		))
	}
	b.WriteString(fmt.Sprintf("        oifname \"%s\" masquerade\n", cfg.DevWAN))
	b.WriteString("    }\n\n")

	// forward filter
	b.WriteString("    chain forward {\n")
	b.WriteString("        type filter hook forward priority filter; policy accept;\n")
	b.WriteString("        ct state established,related accept\n")
	b.WriteString(fmt.Sprintf("        iifname \"%s\" oifname \"%s\" accept\n", cfg.DevLAN, cfg.DevWAN))
	b.WriteString(fmt.Sprintf("        iifname \"%s\" oifname \"%s\" accept\n", cfg.DevWAN, cfg.DevLAN))
	// 非对称回程：公网源直达 LAN 内网段丢弃
	for _, cidr := range routes {
		b.WriteString(fmt.Sprintf(
			"        iifname \"%s\" oifname \"%s\" ip daddr %s ip saddr != %s drop\n",
			cfg.DevWAN, cfg.DevLAN, cidr, cidr,
		))
	}
	b.WriteString("    }\n\n")

	// input（基础）
	b.WriteString("    chain input {\n")
	b.WriteString("        type filter hook input priority filter; policy accept;\n")
	b.WriteString("        iifname \"lo\" accept\n")
	b.WriteString("        ct state established,related accept\n")
	b.WriteString(fmt.Sprintf("        iifname \"%s\" accept\n", cfg.DevLAN))
	b.WriteString("    }\n}\n")
	return b.String(), nil
}

// Apply 生成、校验并加载 nftables
func Apply(cfg Config, st store.State) error {
	body, err := Render(cfg, st)
	if err != nil {
		return err
	}
	if err := os.MkdirAll(filepath.Dir(RulesPath), 0755); err != nil {
		return err
	}
	if err := os.WriteFile(RulesPath, []byte(body), 0644); err != nil {
		return err
	}
	if out, err := exec.Command("nft", "-c", "-f", RulesPath).CombinedOutput(); err != nil {
		return fmt.Errorf("nft check: %s %w", strings.TrimSpace(string(out)), err)
	}
	if out, err := exec.Command("nft", "-f", RulesPath).CombinedOutput(); err != nil {
		return fmt.Errorf("nft load: %s %w", strings.TrimSpace(string(out)), err)
	}
	return nil
}

func sortedKeys(m map[string]string) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}

// AddSharedIP 追加共享 IP（API 用）
func AddSharedIP(st *store.State, ip string) error {
	if netParseIP(ip) == nil {
		return fmt.Errorf("invalid ip: %s", ip)
	}
	for _, e := range st.SharedIPs {
		if e == ip {
			return nil
		}
	}
	st.SharedIPs = append(st.SharedIPs, ip)
	return nil
}

func netParseIP(s string) error {
	parts := strings.Split(s, ".")
	if len(parts) != 4 {
		return fmt.Errorf("bad ip")
	}
	for _, p := range parts {
		n, err := strconv.Atoi(p)
		if err != nil || n < 0 || n > 255 {
			return fmt.Errorf("bad ip")
		}
	}
	return nil
}
